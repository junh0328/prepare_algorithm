# Separator 옵션 사용 (sep에 해당하는 문자열을 바탕으로 합쳐주겠다)

print('T', 'E', 'S', 'T')
print('T', 'E', 'S', 'T', sep='')
print('2019', '02', '19')
print('2019', '02', '19', sep='-')
print('niceman', 'google.com', sep='@')

# end 옵션 사용 (끝을 내지 않겠다)
